void fw_main(void)
{
    WRITE_REGISTER(0x40000000, 0xFF);
    WRITE_REGISTER(0x40000400, 0xFF);

    const unsigned char explosion[5][8] = {
        {
            0b00000000,
            0b00000000,
            0b00000000,
            0b00001000,
            0b00010000,
            0b00000000,
            0b00000000,
            0b00000000
        },
        {
            0b00000000,
            0b00001000,
            0b00001000,
            0b00111110,
            0b00010000,
            0b00010000,
            0b00000000,
            0b00000000
        },
        {
            0b00001000,
            0b00011100,
            0b00111110,
            0b01111111,
            0b01111111,
            0b00111110,
            0b00011100,
            0b00001000
        },
        {
            0b00111100,
            0b01111110,
            0b11111111,
            0b11111111,
            0b11111111,
            0b11111111,
            0b01111110,
            0b00111100
        },
        {
            0b00000000,
            0b00000000,
            0b00000000,
            0b00000000,
            0b00000000,
            0b00000000,
            0b00000000,
            0b00000000
        }
    };

    while (1)
    {
        for (int f = 0; f < 5; f++)
        {
            for (int repeat = 0; repeat < 60; repeat++)
            {
                for (int row = 0; row < 8; row++)
                {
                    WRITE_REGISTER(0x40000008, (1 << row));
                    WRITE_REGISTER(0x40000408, ~(explosion[f][row]));
                    delay_us(10);
                    WRITE_REGISTER(0x40000408, 0xFF);
                }
            }
        }
    }
}
